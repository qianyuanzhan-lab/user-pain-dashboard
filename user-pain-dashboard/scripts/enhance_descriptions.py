def main():
    # 读取原始数据
    input_path = 'data/processed/wechat_ai_opportunities_consolidated.json'
    with open(input_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # 更新描述和 AI 建议
    updated_count = 0
    for opp in data['ai_opportunities']:
        title = opp['title']
        if title in ENHANCED_DATA:
            opp['description'] = ENHANCED_DATA[title]['description']
            opp['ai_suggestion'] = ENHANCED_DATA[title]['ai_suggestion']
            updated_count += 1
            print(f'✓ 已更新: {title}')
        else:
            print(f'⚠ 未找到增强数据: {title}')
            # 保留原描述，添加空的 AI 建议
            if 'ai_suggestion' not in opp:
                opp['ai_suggestion'] = ''
    
    # 保存更新后的数据
    with open(input_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f'\n共更新 {updated_count} 条需求')

if __name__ == '__main__':
    main()
